package decisionMakingAndLoops;

public class DisplayAlphabets {

	public static void main(String[] args) {
		
		char ch;
		for(ch ='A';ch<='Z';ch++) {
			System.out.print(ch + " ");
		}
		System.out.println();
		char c;
		for(c ='a';c<='z';c++) {
			System.out.print( c + " ");
		}
	}

}
